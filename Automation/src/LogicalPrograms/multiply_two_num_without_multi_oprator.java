package LogicalPrograms;

public class multiply_two_num_without_multi_oprator {

	public static void main(String[] args) {
		int num1 = 6;
		int num2 = 3;
		
		int sum = 0;
		
		for(int i=1; i<=num2; i++) {
			sum=sum+num1;
			
			//multiplication without useing multiplication operator
		}
		System.out.println(sum); //1800
	}
}
